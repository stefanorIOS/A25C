#include <stdio.h>

int main() {
    char c1 = '5';  // Número como carácter
    char c2 = 'c';  // Letra minúscula
    char c3 = 'P';  // Letra mayúscula
    char c4 = '\t'; // Tabulador
    char c5 = '\n'; // Salto de línea

    printf("Carcter 1: %c\n", c1);
    printf("Carcter 2: %c\n", c2);
    printf("Carcter 3: %c\n", c3);
    printf("Carcter 4: %c",c4);
    printf("Carcter 5: %c",c5);

    printf("Ahora el cursor debe estar en una nueva línea después del salto de línea.\n");

    return 0;
}

